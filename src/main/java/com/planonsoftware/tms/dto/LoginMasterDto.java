package com.planonsoftware.tms.dto;

import java.util.Date;

public class LoginMasterDto {
	private String userName;
	private String fullName;
	private String errorMsg;
	private String address;
	private String joiningDate;
	private int noOfOrderTillNow;
	private String phno;
	private String email;
	private String password;
	
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
	public String getJoiningDate() {
		return joiningDate;
	}
	public void setJoiningDate(String joiningDate) {
		this.joiningDate = joiningDate;
	}
	public int getNoOfOrderTillNow() {
		return noOfOrderTillNow;
	}
	public void setNoOfOrderTillNow(int noOfOrderTillNow) {
		this.noOfOrderTillNow = noOfOrderTillNow;
	}
	public String getPhno() {
		return phno;
	}
	public void setPhno(String phno) {
		this.phno = phno;
	}
	public String getErrorMsg() {
		return errorMsg;
	}
	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	
	
}
